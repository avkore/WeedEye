							<h2>Pennsylvania Police Make More Than 20,000 Pot Arrests During The Pandemic</h2>
							<p>Police in Pennsylvania made 20,200 arrests for marijuana possession in 2020, a year marked by the coronavirus pandemic and the subsequent designation of cannabis businesses as essential services in many jurisdictions. Arrest data from the Pennsylvania State Police showed that an average of 55 adults were arrested for cannabis possession in the state every day last year. </p>
							<p>The data, which includes arrests by state and local police throughout the Keystone State, was acquired by Chris Goldstein, a regional coordinator with the National Organization for the Reform of Marijuana Laws (NORML). Goldstein helped draft Philadelphia’s 2014 cannabis decriminalization ordinance, a move that was followed by action to reform marijuana laws in more than a dozen additional Pennsylvania cities, including the state capital of Harrisburg.</p>
							<p>“Cannabis consumers were targeted even during the Covid-19 pandemic,” Goldstein said in a statement from NORML. “This shows just how aggressively prohibition is enforced, despite the unprecedented public health risks in our communities. It’s time to stop marijuana arrests, right now.”</p>
							<p>The data from law enforcement shows that police in Pennsylvania made more arrests for marijuana possession than for all other illegal substances combined, which totaled 17,425 arrests. </p>
							<h2>Racial Disparity In Drug Enforcement Continues</h2>
							<p>An analysis of the data showed a wide racial disparity in the marijuana possession arrests made in Pennsylvania last year. Although Black people make up only 12% of the state’s population, 32% of those arrested for marijuana were Black. The imbalance comes despite studies that have consistently shown comparable rates of cannabis use among Black people and white people.</p>
							<p>“Marijuana should not be used as an excuse for law enforcement to interact with otherwise law-abiding members of the public, especially during a global pandemic,” said NORML State Policies Manager Carly Wolf. “The ongoing prohibition of marijuana in Pennsylvania encroaches upon civil liberties and disproportionately impacts communities of color. It’s time for lawmakers to take action so that marijuana consumers are no longer treated as second-class citizens.”</p>
							<p>Further analysis reveals that 461 Black people were arrested for marijuana possession for every 100,000 of Pennsylvania’s population, while only 131 white people were arrested for pot per 100,000 residents. Taken together, the per capita figures show that Black people are about 3.5 times more likely to be arrested for marijuana possession in Pennsylvania than their white counterparts. Jeff Riedy, the executive director of Lehigh Valley NORML, noted that the racial disparity in arrest data in Pennsylvania mirrors the disproportionate enforcement of drug laws across the United States.</p>
							<p>“These numbers remind us of the continued impact of the criminalization of cannabis at home while reflecting the racial disparity among arrests nationwide,” Riedy said in the statement from the cannabis policy reform advocacy group. “The only real solution is enacting immediate decriminalization and eventual legalization of cannabis in Pennsylvania. We know that too many people’s lives are ruined for a simple possession charge, and that must end now! We’re talking about a plant.”</p>
